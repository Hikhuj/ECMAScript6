// Sum of two digits
export var add = function(n1, n2) {
    return n1 + n2;
}

// Division of two digits
export var divide = function(n1, n2) {
    return n1 / n2;
}

// Subtraction of two digits
export var subtract = function(n1, n2) {
    return n1 - n2;
}

// Multiplication of two digits
export var multiply = function(n1, n2) {
    return n1 * n2;
}

